function sort(arr) {
   // 
   let arr_index = [];
   let arr_sort = [];
   let max = Math.max(...arr) + 1;
   while( arr_index.length < arr.length ) {
      let min = Math.min(...arr);
      let index = arr.indexOf(min);
      arr_index.push(index);
      arr_sort.push(min);
      arr[index] = max;
   }
   return [arr_sort, arr_index];
}

